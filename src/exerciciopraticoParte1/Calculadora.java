package exerciciopraticoParte1;

public class Calculadora {

	public static void soma(double a1, double a2) {
			double resultado = a1+a2;
			System.out.println("SOMA: "+a1+" + "+a2+" = "+ resultado);
			
	}
	
	public static void divisao(double a1, double a2) {
		 double resultado = a1/a2;
		 System.out.println("DIVISAO: "+a1+" / "+a2+" =" + resultado);

	}
	public static void multiplica(double a1, double a2) {
		 double resultado = a1*a2;
		 System.out.println("MULTIPLICAÇÃO: "+a1+" X "+a2+" = "+ resultado);

	}
	public static void subtracao(double a1, double a2) {
		 double resultado = a1-a2;
		 System.out.println("SUBTRAÇÃO: "+a1+" - "+a2+" = "+ resultado);

	}

	
	
	
}
